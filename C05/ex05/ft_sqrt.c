int	ft_sqrt(int nb)
{
	int	rs;

	rs = 0;
	if (nb < 0)
		return (0);
	while (rs < 46341)
	{
		rs++;
		if ((rs * rs) == nb)
			return (rs);
	}
	return (0);
}
